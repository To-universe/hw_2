void setup() {
  // OLED初始化
  OLED.begin(SSD1306_SWITCHCAPVCC, SCREEN_ADDRESS);
  // OLED清除显示
  OLED.clearDisplay();
  // OLED设置光标位置
  OLED.setCursor(1, 1);
  //设置字体大小
  OLED.setTextSize(1);
  // 设置字体颜色
  OLED.setTextColor(SSD1306_WHITE);
  // 显示字符串内容
  OLED.println("Hello,world!");
  OLED.println("Hello,Xlab!");
  OLED.println("Hello,Arduino.");

  OLED.display();

  //字符滚动
  OLED.startscrollright(0x00,0x07);
  delay(2000);
  OLED.startscrollleft(0x00,0x00);
  delay(2000);
  OLED.startscrollleft(0x01,0x07);
  delay(2000);
  OLED.stopscroll();
  OLED.display();

  delay(10000);
  OLED.clearDisplay();
  OLED.drawBitmap(0,0,epd_bitmap__________modified,128,64,WHITE);
  OLED.display();
}

